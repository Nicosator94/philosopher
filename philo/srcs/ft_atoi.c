long	ft_atoi(const char *nptr)
{
	int		i;
	long	nb;

	i = 0;
	nb = 0;
	if (nptr[i] >= '0' && nptr[i] <= '9')
	{
		while (nptr[i] >= '0' && nptr[i] <= '9')
		{
			nb = nb * 10;
			nb = nb + nptr[i] - 48;
			i ++;
		}
		return (nb);
	}
	return (0);
}
